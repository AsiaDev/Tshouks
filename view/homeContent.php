	<h2>Home</h2>
	<h3>What is Tshouks?</h3>
	<div class="contentBox">
		Tshouks ist eine Webseite, die als Projekt von 2 Informatik-Lehrlingen
		erstellt wurde. Unsere Webseite handelt über eine ganz simple
		Witze-Seite. Hier sind alle Witze möglich, von ganz simplen Witzen,
		wie man sie aus Zeitungen kennt, bis hin zu Witzen mit dem
		schwarzesten Humor. Tipp: Wer sich schnell beleidigt fühlen kann
		oder angegriffen/ grausam dabei fühlt möge die Webseite bitte
		verlassen, wir haben euch gewarnt. Zusätzlich zu unseren eigenen
		Standard-Witzen, darf sich jeder hier gerne einen Account zulegen,
		also registrieren und danach einloggen.<br> Nun was habt ihr vom
		Einloggen? Ganz einfach: Ihr dürft uns helfen unsere Webseite mehr zu
		füllen indem ihr eure eigenen Witze postet. Missbraucht aber diese
		bitte nicht, wir sind euch dankbar, dass ihr unsere Webseite besucht
		habt und würden uns freuen, wenn ihr ebenfalls einen tollen und coolen
		Witz hinterlasst. Ihr könnt uns über das Kontaktformular gerne ein
		Feedback schicken und/oder Vorschläge für Verbesserungen bringen. MfG
		das B+A Team
	</div>
	<h3>Unser Grundsatz</h3>
	<div class="contentBox">Witze sind dazu da um darüber zu lachen, nicht
		um Leute zu diskriminieren. Unserer Meinung nach ist jede Art von
		Humor akzeptabel solange sie nich störend ist. Bis jetzt kann jeder
		Witze posten, bitte nicht ausnutzen, wir sehen uns sonst gezwungen
		diese Funktion zu ändern.</div>

